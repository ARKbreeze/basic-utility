/**
 * @desc 两数相加
 * @param {Number} num1
 * @param {Number} num2
 * @return {Number} 返回两数相加结果
 */
function add(num1, num2) {
  return num1 + num2;
}

/**
 * @desc 自增
 * @param {Number} num
 * @return {Number} 返回参数加一
 */
function increment(num) {
  return num + 1;
}

export default {
  add,
  increment
};
